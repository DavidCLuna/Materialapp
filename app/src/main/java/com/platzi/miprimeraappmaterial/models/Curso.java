public class Curso {

    String titulo;
    String descripción;
    String tipoDeBagde;

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    public String getDescripción() {
        return descripción;
    }

    public void setDescripción(String descripción) {
        this.descripción = descripción;
    }

    public String getTipoDeBagde() {
        return tipoDeBagde;
    }

    public void setTipoDeBagde(String tipoDeBagde) {
        this.tipoDeBagde = tipoDeBagde;
    }






}
